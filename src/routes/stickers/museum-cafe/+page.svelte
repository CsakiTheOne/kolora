<PoemDisplay author="Ady Endre" title="Kávéházban">
Kávéházi sarokasztal,
Körülüljük szépen.
Így szoktuk azt minden este,
Így szoktuk azt négyen.
Diskurálunk, elpletykázunk
Egyről-másról, sorba -
Másképpen a téli este
Rém unalmas volna.
    
Egyik szinész, másik költő...
Összeillik szépen.
Valamennyi bohém fiú,
Van miről beszéljen.
A hír és név, a dicsőség
Valamennyi célja...
Mégis, mégis úgy elborong
A négy fiú néha...
    
És ilyenkor biztatgatjuk,
Vigasztaljuk egymást.
Másra visszük a beszédet:
- Lesz-e holnap telt ház?
- A szép Lili kap-e csokrot?
- Írt a hadnagyának?
...Addig, addig beszélgetünk,
Míg elfut a bánat.
    
Olykor-olykor kijelöljük,
Hogy ki beszél máma,
Mindeniknek van valami
Jó históriája.
Elsiratott ábránd éled
Mindenik mesébe',
Visszaint az ijfuságnak
Messze bolygó fénye.
    
Egyik este véletlenül
Én jöttem a sorba,
Mesélni a féltett multról
Nekem kellett volna.
Amit talizmánná tesznek
Hosszu, kínos évek:
A legelső szerelemről
Szóljon hát az ének!...
    
Belekezdtem... De egyszerre
Megbénult a lelkem,
Feltárult a mult előttem
Emlékekkel telten...
Egy kis város, ahol egykor
Olyan boldog voltam,
Egy szép leány, kiről egykor
Annyiszor daloltam...
    
Elszédített ennyi emlék,
Szemem könnybe lábadt...
- Mért is kérdeztétek az én
Bús históriámat?...
Bánat ült a fiuk arcán,
Elhallgattak szépen...
Ott csillogott az a könnycsepp
Mindnyájunk szemében...
</PoemDisplay>

<PoemDisplay author="Somogyi Csaba" title="Tavasz a mosogatóban">
A konyhában csend van,
a mirelit magány a nyakamba olvad.
Arcod sárgult abrosz,
kések a fiókban, tányérok a polcon.
Teríteni kéne.
A tavaszt a mosogatóban hagytam,
étvágytalanság csillog a szememben.
A hűtőt kihúztam.
Néha eszembe jut a gyomormosás,
ha enni vágyom.
</PoemDisplay>

<PoemDisplay author="Petőfi Sándor" title="Dicsőséges nagyurak">
Dicsőséges nagyurak, hát
Hogy vagytok?
Viszket-e ugy egy kicsit a
Nyakatok?
Uj divatu nyakravaló
Készül most
Számotokra... nem cifra, de
Jó szoros.
    
Tudjátok-e, mennyit kértünk
Titeket,
Hogy irántunk emberiek
Legyetek,
Vegyetek be az emberek
Sorába...
Rimánkodott a szegény nép,
S hiába.
    
Állatoknak tartottátok
A népet;
Hát ha most mint állat fizet
Tinéktek?
Ha megrohan, mint vadállat
Bennetek,
S körmét, fogát véretekkel
Festi meg?
    
Ki a síkra a kunyhókból
Miljomok!
Kaszát, ásót, vasvillákat
Fogjatok!
Az alkalom maga magát
Kinálja,
Ütött a nagy bosszuállás
Órája!
    
Ezer évig híztak rajtunk
Az urak,
Most rajtok a mi kutyáink
Hízzanak!
Vasvillára velök, aztán
Szemétre,
Ott egyék a kutyák őket
Ebédre!...
    
Hanem még se!... atyafiak,
Megálljunk!
Legyünk jobbak, nemesebbek
Őnáluk;
Isten után legszentebb a
Nép neve:
Feleljünk meg becsülettel
Nékie.
    
Legyünk nagyok, amint illik
Mihozzánk,
Hogy az isten gyönyörködve
Nézzen ránk,
S örömében mindenható
Kezével
Fejeinkre örök áldást
Tetézzen.
    
Felejtsük az ezer éves
Kínokat,
Ha az úr most testvérének
Befogad;
Ha elveti kevélységét,
Címerit,
S teljes egyenlőségünk el-
Ismeri.
    
Nemes urak, ha akartok
Jőjetek,
Itt a kezünk, nyujtsátok ki
Kezetek.
Legyünk szemei mindnyájan
Egy láncnak,
Szüksége van mindnyájunkr' a
Hazának.
    
Nem érünk rá várakozni,
Szaporán,
Ma jókor van, holnap késő
Lesz talán.
Ha bennünket még mostan is
Megvettek,
Az uristen kegyelmezzen
Tinektek!
</PoemDisplay>

<PoemDisplay author="Le Peter" title="Meggyvirág">
Meggyvirág tiltott illata,
Felettesd el velem búmat.
Felettesd velem bánatom,
Múljon végre a fájdalom.

Emlékszem arra, milyen volt
Akkor érezni illatod.
Oly szép lett tőled a világ,
Nem kívántam már semmi mást.

Csak azt hogy ne érjen véget,
Mint minden más, te ne hagyj el.
Ásd magad mélyen szívembe,
Hozz fényt megkínzott lelkembe.

Tudom jól, a túl sok: átok,
Részegít, többé nem látok.
Bár nincs mit látni e földön,
Csak sötétet a nagy ködtől.

Részegíts hát, a köd szálljon,
Messze el, hol nem találom.
Csak te légy nekem, Meggyvirág,
Tiéd leszek, s szebb a világ.

Múljon végre a fájdalom,
Felettesd velem bánatom.
Felettesd el velem búmat,
Meggyvirág tiltott illata.
</PoemDisplay>

<PoemDisplay author="Kosztolányi Dezső" title="Nyár nyár nyár">
Nyár,
A régi vágyam egyre jobban
Lobban,
De vár, még egyre vár.
    
Kár
Így késlekedned, mert az éj setétül.
Az élet
Siralmas és sivár
Enélkül.
    
Gigászi vágyam éhes, mint a hörcsög,
Görcsök
Emésztik s forró titkom mélye szörcsög.
    
Mostan hajolj feléje.
Közel a lázak kéjes éje.
Akarod?
Remegve nyújtsd a szájad és karod.
Itt ital illatja tégedet vár.
Nektár.
    
Te
Hűtelen, boldog leszel majd újra, hidd meg.
Idd meg.
</PoemDisplay>

<PoemDisplay author="Radnóti Miklós" title="Razglednicák">
1
Bulgáriából vastag, vad ágyuszó gurul,
a hegygerincre dobban, majd tétováz s lehull;
torlódik ember, állat, szekér és gondolat,
az út nyerítve hőköl, sörényes ég szalad.
Te állandó vagy bennem e mozgó zürzavarban,
tudatom mélyén fénylesz örökre mozdulatlan
s némán, akár az angyal, ha pusztulást csodál,
vagy korhadt fának odván temetkező bogár.
1944. augusztus 30. A hegyek közt
    
2
Kilenc kilométerre innen égnek
a kazlak és a házak,
s a rétek szélein megülve némán
riadt pórok pipáznak.
Itt még vizet fodroz a tóra lépő
apró pásztorleány
s felhőt iszik a vízre ráhajolva
a fodros birkanyáj.
Cservenka,1944. október 6.
    
3
Az ökrök száján véres nyál csorog,
az emberek mind véreset vizelnek,
a század bűzös, vad csomókban áll.
Fölöttünk fú a förtelmes halál.
Mohács,1944. október 24.
    
4
Mellézuhantam, átfordult a teste
s feszes volt már, mint húr, ha pattan.
Tarkólövés. - Így végzed hát te is, -
súgtam magamnak, - csak feküdj nyugodtan.
Halált virágzik most a türelem. -
Der springt noch auf,* - hangzott fölöttem.
Sárral kevert vér száradt fülemen.
Szentkirályszabadja,1944. október 31.
